    private void cadastraPaciente() throws ValorInvalidoException, ErroCadastroException {
        String nome = textPacienteAdicionarNome.getText();
        String telefone = textPacienteAdicionarTelefone.getText();
        String nascimento = textPacienteAdicionarNascimento.getText();
        String cpf = textPacienteAdicionarCPF.getText();
        String endereco = textPacienteAdicionarEndereco.getText();
        Paciente paciente = new Paciente();
    
        paciente.setNome(nome);
        paciente.setTelefone(Integer.parseInt(telefone));
        paciente.setNascimento(nascimento);
        paciente.setCpf(Integer.parseInt(cpf));
        paciente.setEndereco(endereco);
        
        
        new ManipuladorPacientes().cadastra(paciente);
    }
    
    private void limpaCampos() {
        textPacienteAdicionarNome.setText("");
        textPacienteAdicionarTelefone.setText("");
        textPacienteAdicionarNascimento.setText("");
        textPacienteAdicionarCPF.setText("");
        textPacienteAdicionarEndereco.setText("");
    }
    
    private void verificaCadastrarNovamente() {
        
        int respUsuario = JOptionPane.showConfirmDialog(rootPane,
                "Deseja cadastrar outro "
                + "paciente?", "Mensagem", JOptionPane.YES_NO_OPTION);
        
        switch (respUsuario) {
            
            case JOptionPane.YES_OPTION:
                limpaCampos();
                break;
            case JOptionPane.NO_OPTION:
                btPacienteAdicionarVoltar.doClick();
                limpaCampos();
                break;
            default:
                break;
        }
    }
    private boolean passouNaPreValidacaoPaciente() {
        String nome = textPacienteAdicionarNome.getText();
        String telefone = textPacienteAdicionarTelefone.getText();
        String nascimento = textPacienteAdicionarNascimento.getText();
        String cpf = textPacienteAdicionarCPF.getText();
        String endereco = textPacienteAdicionarEndereco.getText();

        if (nome.isEmpty() || telefone.isEmpty() || nascimento.isEmpty()|| cpf.isEmpty() || endereco.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nem todos os campos foram "
                    + "preenchidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }
